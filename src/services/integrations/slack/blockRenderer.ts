// ================================================================
// Sprint D-9 — Slack Block Kit renderer for insights + KPI replies.
//
// Decision §10.G option G1 — text-only blocks with severity emoji
// header, no PNG generation. Matches ~95% of the spec mock's visual
// impact via header text + mrkdwn weight; promote to G2 (D-7 OG-style
// PNG header) in V2 if Slack engagement metrics warrant the cost.
//
// Outputs the { text, blocks } shape consumed by chat.postMessage.
// `text` is the fallback for clients that strip blocks (notification
// previews, mobile shortcuts, archived workspaces). Always populate
// it — Slack downgrades to text-only when blocks fail to render.
// ================================================================

export type Locale = "tr" | "en" | "ar";

export type SlackBlock =
  | { type: "header";  text: { type: "plain_text"; text: string; emoji?: boolean } }
  | { type: "section"; text?: { type: "mrkdwn" | "plain_text"; text: string }; fields?: Array<{ type: "mrkdwn"; text: string }> }
  | { type: "context"; elements: Array<{ type: "mrkdwn" | "plain_text"; text: string }> }
  | { type: "divider" }
  | { type: "actions"; block_id?: string; elements: SlackButton[] };

export type SlackButton = {
  type:      "button";
  text:      { type: "plain_text"; text: string; emoji?: boolean };
  action_id: string;
  value?:    string;
  url?:      string;
  style?:    "primary" | "danger";
};

export interface InsightForSlack {
  id:           string;
  type:         "CRITICAL" | "ATTENTION" | "OPPORTUNITY";
  title:        string;
  body:         string;
  ctaLabel?:    string | null;
  ctaRoute?:    string | null;
  numericRefs?: unknown;
  generatedAt?: Date;
}

export interface RenderInsightArgs {
  insight:    InsightForSlack;
  locale:     Locale;
  /** Where the "View in FinSuite" button links to. */
  appBaseUrl: string;
}

