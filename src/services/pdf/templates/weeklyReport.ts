function renderSummaryPage(args: {
  data: WeeklyReportTemplateData; theme: Theme; locale: Locale;
  fmtCur: (n: number) => string; fmtPct: (n: number) => string;
}): string {
  const { data, theme, locale, fmtCur, fmtPct } = args;
  const pal = paletteOf(theme);
  const k = data.snapshot.kpis;

  const kpis = [
    { label: T('mrr', locale),     value: k.mrr.value,     tone: 'cyan'    as Tone, format: fmtCur, delta: k.mrr.deltaPct },
    { label: T('netCash', locale), value: k.netCash.value, tone: 'mint'    as Tone, format: fmtCur, delta: k.netCash.deltaPct },
    { label: T('margin', locale),  value: k.margin.value,  tone: 'violet'  as Tone, format: (n: number) => `${n.toFixed(1)}%`, delta: k.margin.deltaPct },
    { label: T('runway', locale),  value: k.runway.value,  tone: 'amber'   as Tone, format: (n: number) => `${Math.round(n)}d`, delta: k.runway.deltaPct }
  ];

  const insightsHtml = data.insights.length > 0
    ? data.insights.slice(0, 3).map((i) => `
        <div style="margin-bottom: 8pt;">
          ${aiInsightCard({
            severity:    i.type.toLowerCase() as any,
            title:       i.title,
            description: i.body,
            actionLabel: i.ctaLabel,
            badgeText:   T(i.type, locale),
            theme
          })}
        </div>`).join('')
    : `<div class="card" style="text-align: center; padding: 24pt; color: ${pal.textFaint}; font-size: 10pt;">${esc(T('noInsights', locale))}</div>`;

  return `
    <div class="section-page">
      <div class="section-title-row">
        <div>
          <div class="t-caption" style="color: ${pal.textFaint};">${esc(T('weeklyTitle', locale))}</div>
          <div class="t-display-md" style="margin-top: 4pt;">${esc(T('brief', locale))}</div>
        </div>
        <div style="text-align: ${locale === 'ar' ? 'left' : 'right'};">
          <div class="t-caption" style="color: ${pal.textFaint};">${esc(T('dateRange', locale))}</div>
          <div class="t-heading-md t-dim" style="margin-top: 4pt;">
            ${esc(formatDate(data.weekStart, locale))} — ${esc(formatDate(data.weekEnd, locale))}
          </div>
        </div>
      </div>

      <div class="narrative-block">${esc(data.narrative)}</div>

      <div class="page-break-avoid mb-4">
        ${constellationKpiGrid({ kpis, theme })}
      </div>

      <div class="t-caption mb-2" style="color: ${pal.textFaint};">${esc(T('insightsLabel', locale))}</div>
      <div class="page-break-avoid">${insightsHtml}</div>

      ${footer({ merchantName: data.merchantName, generatedAt: new Date(), locale })}
    </div>
  `;
}

function renderRevenuePage(args: {
  data: WeeklyReportTemplateData; theme: Theme; locale: Locale;
  fmtCur: (n: number) => string; fmtPct: (n: number) => string;
}): string {
  const { data, theme, locale, fmtCur, fmtPct } = args;
  const pal = paletteOf(theme);
  const r = data.snapshot.revenue;
  const k = data.snapshot.kpis;

  const series = r.dailySeries.map((d) => ({ x: d.date.slice(5), y: d.amount }));

  const top = r.topCustomers.slice(0, 5);
  const topTotal = top.reduce((s, c) => s + c.amount, 0);
  const customerRows = top.length > 0
    ? top.map((c) => {
        const pct = topTotal > 0 ? (c.amount / topTotal) * 100 : 0;
        return `
          <tr>
            <td>${esc(c.name)}</td>
            <td style="text-align: right; font-weight: 700;">${esc(fmtCur(c.amount))}</td>
            <td style="text-align: right; color: ${pal.textFaint};">${pct.toFixed(0)}%</td>
            <td style="width: 40%;">
              <div style="height: 6pt; background: ${pal.border}; border-radius: 3pt;">
                <div style="height: 100%; width: ${pct.toFixed(0)}%; background: ${toneColor('cyan')}; border-radius: 3pt;"></div>
              </div>
            </td>
          </tr>`;
      }).join('')
    : `<tr><td colspan="4" style="text-align: center; color: ${pal.textFaint}; padding: 16pt;">${esc(T('noCustomers', locale))}</td></tr>`;

  return `
    <div class="section-page">
      <div class="section-title-row">
        <div>
          <div class="t-caption" style="color: ${pal.textFaint};">${esc(T('weeklyTitle', locale))}</div>
          <div class="t-display-md" style="margin-top: 4pt;">${esc(T('revenueDive', locale))}</div>
        </div>
      </div>

      <div class="page-break-avoid" style="margin-bottom: 16pt;">
        ${auroraChart({ data: series, tone: 'cyan', width: 520, height: 200, theme })}
      </div>

      <div class="stat-strip">
        <div class="stat-cell">
          <div class="lbl">${esc(T('totalRevenue', locale))}</div>
          <div class="val">${esc(fmtCur(r.total))}</div>
        </div>
        <div class="stat-cell">
          <div class="lbl">${esc(T('avgDaily', locale))}</div>
          <div class="val">${esc(fmtCur(r.avgDaily))}</div>
        </div>
        <div class="stat-cell">
          <div class="lbl">${esc(T('bestDay', locale))}</div>
          <div class="val">${r.bestDay ? esc(fmtCur(r.bestDay.amount)) : '—'}</div>
        </div>
        <div class="stat-cell">
          <div class="lbl">${esc(T('vsLastWeek', locale))}</div>
          <div class="val" style="color: ${k.mrr.deltaPct >= 0 ? '#06A87E' : '#FF3D5A'};">${esc(fmtPct(k.mrr.deltaPct))}</div>
        </div>
      </div>

      <div class="t-caption mb-2" style="color: ${pal.textFaint};">${esc(T('topCustomers', locale))}</div>
      <table class="zyrix-table page-break-avoid">
        <tbody>${customerRows}</tbody>
      </table>

      ${footer({ merchantName: data.merchantName, generatedAt: new Date(), locale })}
    </div>
  `;
}

function renderCashPage(args: {
  data: WeeklyReportTemplateData; theme: Theme; locale: Locale;
  fmtCur: (n: number) => string;
}): string {
  const { data, theme, locale, fmtCur } = args;
  const pal = paletteOf(theme);
  const c = data.snapshot.cash;
  const k = data.snapshot.kpis;

  // Margin trend approximation: 4-week mock from current value (we only
  // have one window); render as a flat sparkline placeholder.
  const marginTrend = [
    Math.max(0, k.margin.value - k.margin.delta * 3),
    Math.max(0, k.margin.value - k.margin.delta * 2),
    Math.max(0, k.margin.value - k.margin.delta),
    Math.max(0, k.margin.value)
  ];

  const expenseRows = c.topExpenses.length > 0
    ? c.topExpenses.map((e) => `
        <tr>
          <td>${esc(e.category)}</td>
          <td style="color: ${pal.textFaint}; font-size: 9pt;">${esc(e.date)}</td>
          <td style="text-align: right; font-weight: 700; color: #FF3D5A;">${esc(fmtCur(e.amount))}</td>
        </tr>`).join('')
    : `<tr><td colspan="3" style="text-align: center; color: ${pal.textFaint}; padding: 16pt;">${esc(T('noExpenses', locale))}</td></tr>`;

  return `
    <div class="section-page">
      <div class="section-title-row">
        <div>
          <div class="t-caption" style="color: ${pal.textFaint};">${esc(T('weeklyTitle', locale))}</div>
          <div class="t-display-md" style="margin-top: 4pt;">${esc(T('cashMargin', locale))}</div>
        </div>
      </div>

      <div class="page-break-avoid" style="margin-bottom: 14pt;">
        ${flowStream({
          inflows:  [{ name: T('inflows',  locale), value: c.inflowTotal  }],
          outflows: [{ name: T('outflows', locale), value: c.outflowTotal }],
          width: 520, height: 180, theme
        })}
      </div>

      <div class="stat-strip" style="grid-template-columns: repeat(3, 1fr);">
        <div class="stat-cell">
          <div class="lbl">${esc(T('inflows', locale))}</div>
          <div class="val" style="color: #06A87E;">${esc(fmtCur(c.inflowTotal))}</div>
        </div>
        <div class="stat-cell">
          <div class="lbl">${esc(T('outflows', locale))}</div>
          <div class="val" style="color: #FF3D5A;">${esc(fmtCur(c.outflowTotal))}</div>
        </div>
        <div class="stat-cell">
          <div class="lbl">${esc(T('netFlow', locale))}</div>
          <div class="val" style="color: ${c.netFlow >= 0 ? '#06A87E' : '#FF3D5A'};">${esc(fmtCur(c.netFlow))}</div>
        </div>
      </div>

      <div class="page-break-avoid" style="margin-bottom: 14pt;">
        <div class="t-caption mb-2" style="color: ${pal.textFaint};">${esc(T('margin', locale))}</div>
        ${pulseSparkline({ data: marginTrend, severity: 'normal', width: 240, height: 60, theme })}
      </div>

      <div class="t-caption mb-2" style="color: ${pal.textFaint};">${esc(T('topExpenses', locale))}</div>
      <table class="zyrix-table page-break-avoid">
        <tbody>${expenseRows}</tbody>
      </table>

      ${footer({ merchantName: data.merchantName, generatedAt: new Date(), locale })}
    </div>
  `;
}

function renderCustomerTaxPage(args: {
  data: WeeklyReportTemplateData; theme: Theme; locale: Locale;
  fmtCur: (n: number) => string;
}): string {
  const { data, theme, locale, fmtCur } = args;
  const pal = paletteOf(theme);
  const c = data.snapshot.customers;
  const tax = data.snapshot.tax;
  const tones: Tone[] = ['cyan', 'violet', 'mint', 'amber', 'crimson'];

  const concentrationData = c.concentration.slice(0, 5).map((s, i) => ({
    name:  s.name,
    value: s.amount,
    tone:  tones[i % tones.length]
  }));

  const concentrationRiskBlock = c.topCustomerShare > 40
    ? `<div style="background: rgba(255, 61, 90, 0.08); border: 1px solid rgba(255, 61, 90, 0.30);
                   border-radius: 10pt; padding: 12pt 14pt; margin-bottom: 14pt;">
        <div style="font-size: 9pt; font-weight: 700; color: #FF3D5A; text-transform: uppercase; letter-spacing: 0.08em;">
          ${esc(T('concentrationRisk', locale))}
        </div>
        <div style="margin-top: 4pt; font-size: 10pt; color: ${pal.textPrimary};">
          ${esc(c.concentration[0]?.name || '—')} — ${c.topCustomerShare.toFixed(1)}%
        </div>
      </div>`
    : '';

  const taxRows = tax.upcoming.length > 0
    ? tax.upcoming.map((t) => `
        <tr>
          <td>${esc(t.title)}</td>
          <td style="color: ${pal.textFaint}; font-size: 9pt;">${esc(t.dueDate)}</td>
          <td style="text-align: right; font-weight: 700;">${esc(fmtCur(t.amount))}</td>
        </tr>`).join('')
    : `<tr><td colspan="3" style="text-align: center; color: ${pal.textFaint}; padding: 16pt;">${esc(T('noTax', locale))}</td></tr>`;

  return `
    <div class="section-page">
      <div class="section-title-row">
        <div>
          <div class="t-caption" style="color: ${pal.textFaint};">${esc(T('weeklyTitle', locale))}</div>
          <div class="t-display-md" style="margin-top: 4pt;">${esc(T('customerTax', locale))}</div>
        </div>
      </div>

      ${concentrationData.length > 0 ? `
        <div style="display: flex; gap: 24pt; align-items: flex-start; margin-bottom: 16pt;">
          <div style="flex-shrink: 0;">
            ${holographicDonut({
              data: concentrationData,
              width: 200, height: 200, thickness: 26,
              centerSub: T('concentration', locale),
              format: fmtCur,
              theme
            })}
          </div>
          <div style="flex: 1;">
            ${concentrationRiskBlock}
            <table class="zyrix-table">
              <tbody>
                ${concentrationData.map((s) => `
                  <tr>
                    <td><span style="display: inline-block; width: 6pt; height: 6pt; border-radius: 50%; background: ${toneColor(s.tone)}; margin-${locale === 'ar' ? 'left' : 'right'}: 6pt;"></span>${esc(s.name)}</td>
                    <td style="text-align: right;">${esc(fmtCur(s.value))}</td>
                  </tr>`).join('')}
              </tbody>
            </table>
          </div>
        </div>
      ` : ''}

      <div class="t-caption mb-2" style="color: ${pal.textFaint};">${esc(T('upcomingTax', locale))}</div>
      <table class="zyrix-table page-break-avoid">
        <tbody>${taxRows}</tbody>
      </table>

      ${footer({ merchantName: data.merchantName, generatedAt: new Date(), locale })}
    </div>
  `;
}

