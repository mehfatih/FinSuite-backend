// ─── Public data shape ──────────────────────────────────────────

export interface WeeklyKpi { value: number; delta: number; deltaPct: number; }
export interface WeeklyInsight {
  type:      'CRITICAL' | 'ATTENTION' | 'OPPORTUNITY';
  title:     string;
  body:      string;
  ctaLabel?: string;
}
export interface WeeklySnapshotForTemplate {
  weekStart:   string;
  weekEnd:     string;
  currency:    string;
  merchantNew: boolean;
  kpis: { mrr: WeeklyKpi; netCash: WeeklyKpi; margin: WeeklyKpi; runway: WeeklyKpi };
  revenue: {
    total: number; avgDaily: number;
    bestDay:  { date: string; amount: number } | null;
    worstDay: { date: string; amount: number } | null;
    dailySeries: Array<{ date: string; amount: number }>;
    priorSeries: Array<{ date: string; amount: number }>;
    topCustomers: Array<{ name: string; amount: number }>;
  };
  cash: {
    inflowTotal: number; outflowTotal: number; netFlow: number;
    topExpenses: Array<{ category: string; amount: number; date: string }>;
  };
  customers: {
    totalCount: number;
    concentration: Array<{ name: string; amount: number; pct: number }>;
    topCustomerShare: number;
  };
  tax: { upcoming: Array<{ title: string; amount: number; dueDate: string }> };
}

export interface WeeklyReportTemplateData {
  weekStart:    Date;
  weekEnd:      Date;            // inclusive Sunday
  merchantName: string;
  narrative:    string;
  snapshot:     WeeklySnapshotForTemplate;
  insights:     WeeklyInsight[];
}

export function renderWeeklyReportTemplate(args: {
  data:   WeeklyReportTemplateData;
  theme:  Theme;
  locale: Locale;
}): string {
  const { data, theme, locale } = args;
  const pal = paletteOf(theme);
  const sym = data.snapshot.currency === 'TRY' ? '₺' : data.snapshot.currency === 'SAR' ? 'SAR ' : '';
  const fmtCur = (n: number): string => `${sym}${Math.round(n).toLocaleString()}`;
  const fmtPct = (n: number): string => `${n >= 0 ? '+' : ''}${n.toFixed(1)}%`;

  // Foundation Week — single page when there's <7d of data.
  if (data.snapshot.merchantNew) {
    return htmlShell({
      title:  `${T('weeklyTitle', locale)} — ${data.merchantName}`,
      theme, locale,
      body: renderFoundationPage(data, theme, locale),
      extraStyle: SHARED_CSS(pal)
    });
  }

  const cover    = renderCoverPage(data, locale);
  const summary  = renderSummaryPage({ data, theme, locale, fmtCur, fmtPct });
  const revenue  = renderRevenuePage({ data, theme, locale, fmtCur, fmtPct });
  const cash     = renderCashPage   ({ data, theme, locale, fmtCur });
  const custTax  = renderCustomerTaxPage({ data, theme, locale, fmtCur });
  const actions  = renderActionsPage({ data, theme, locale });

  const body = [cover, summary, revenue, cash, custTax, actions].filter(Boolean).join('');

  return htmlShell({
    title:  `${T('weeklyTitle', locale)} — ${data.merchantName} — ${formatDate(data.weekStart, locale)}–${formatDate(data.weekEnd, locale)}`,
    theme, locale,
    body,
    extraStyle: SHARED_CSS(pal)
  });
}

